def generate_matrix(size: int):
    matrix = [[0 for _ in range(size)] for _ in range(size)]
    for i in range(size):
        for j in range(i, size):
            z = random.randint(0, 1)
            if z == 1 and i != j:
                matrix[i][j] = z
                matrix[j][i] = z
    return matrix


def print_matrix(matrix: list[list]):
    for line in matrix:
        print(line)


